    public void llenarInfo() {
        DefaultTableModel modelo1 = (DefaultTableModel) this.tablaAlumnos.getModel();
        modelo1.setRowCount(0); //Limpio mi tabla, antes de llenarla
        
        String cursoActual = this.getControlador().getControladorVentanas().getCodigoCursoActual();
        //System.out.println("Curso Actual" + cursoActual);
        ArrayList<Usuario> alumnos = this.getControlador().getControladorUsuarios().getAlumnos(cursoActual);
        //System.out.println("Alumnos" + alumnos);
        ArrayList<String[]> alumnosRow1 = new ArrayList<String[]>();
        for(Usuario usuario: alumnos) {
            if(usuario.getRol() == 2) {
                alumnosRow1.add(new String[]{ 
                    usuario.getCodigo(), usuario.getNombre(), usuario.getApellido() } );
            }
        }
    
        //Se llenan las tablas
        for (String[] alumno : alumnosRow1) {
            modelo1.addRow(alumno);
        }
    }
    
