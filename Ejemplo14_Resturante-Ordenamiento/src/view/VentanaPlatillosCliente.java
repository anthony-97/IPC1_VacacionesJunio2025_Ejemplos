    public void llenarListaPlatillos() {
        ArrayList<Platillo> platillos = this.getControlador().getModelo().getListaPlatillos();
        this.listaPlatillos.removeAllItems(); //Limpio la lista desplegable
        for(Platillo platillo : platillos) {
            this.listaPlatillos.addItem(platillo.getId()+" - " + platillo.getNombre() + " - " + platillo.getPrecioManoObra());
        }
    }
    
    public void vaciarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) this.tablaOrdenes.getModel();
        modelo.setRowCount(0);
    }

    public void llenarPlatillosListos(Cliente c) {
        this.labelListos.setText("Listos: ");
        for(OrdenTrabajo o: c.getListaOrdenes()) {
            if(o.getEstado() == 3) {
                this.labelListos.setText(this.labelListos.getText() + " " + o.getId());
            }
        }
    }

