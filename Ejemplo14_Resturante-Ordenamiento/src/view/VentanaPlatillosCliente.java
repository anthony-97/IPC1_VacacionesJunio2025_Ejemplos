    private void anadirBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anadirBotonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.tablaOrdenes.getModel();
        modelo.setRowCount(0);
       
        String plat = (String) this.listaPlatillos.getSelectedItem(); //Se obtiene el item seleccionado
        String platillo = plat.split(" - ")[0];
        this.getControlador().getControladorUsuarios().crearOrdenTrabajo(platillo); 
        
        ArrayList<OrdenTrabajo> ordenes = this.getControlador().getClienteActual().obtenerOrdenesSinAsignar();

        ArrayList<String[]> ordenesRow = new ArrayList<String[]>();
        for(OrdenTrabajo orden: ordenes) {
            ordenesRow.add(new String[]{ orden.getId(), orden.getPlatillo(), "1" } );
        }
        
        //Se llena la tabla
        for (String[] orden : ordenesRow) {
            modelo.addRow(orden);
        }
    }//GEN-LAST:event_anadirBotonActionPerformed

