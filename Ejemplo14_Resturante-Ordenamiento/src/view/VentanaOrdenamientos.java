    private void generarReporteBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarReporteBotonActionPerformed
        String algoritmoSeleccionado = (String) this.listaAlgoritmos.getSelectedItem(); //Se obtiene el item seleccionado
        String velocidadSeleccionada = (String) this.listaVelocidades.getSelectedItem(); //Se obtiene el item seleccionadp
        int velocidad = 500; //Velocidad por default -> Alta (ms) 1/2 segundo
        //Seleccion de velocidad
        if (velocidadSeleccionada.equals("Baja")) {
            velocidad = 5000; //5 segundos
        } else if(velocidadSeleccionada.equals("Media")) {
            velocidad = 2500; //2.5 segundos
        }
        
        this.getControlador().desordenarIngredientes(); //Desordenando, para poder ordenarlos otra vez
        //Seleccion del algoritmo
        if (algoritmoSeleccionado.equals("Burbuja")) {
            //this.getControlador().ordenar(velocidad, 1);
        } else if(algoritmoSeleccionado.equals("QuickSort")) {
            this.getControlador().ordenar(velocidad, 2);
        }
    }//GEN-LAST:event_generarReporteBotonActionPerformed

