    public void llenarListas() {
        this.listaAlgoritmos.removeAllItems(); //Limpio la lista desplegable
        this.listaAlgoritmos.addItem("Burbuja");
        this.listaAlgoritmos.addItem("QuickSort");
        
        this.listaVelocidades.removeAllItems(); //Limpio la 2 lista desplegable
        this.listaVelocidades.addItem("Alta");
        this.listaVelocidades.addItem("Media");
        this.listaVelocidades.addItem("Baja");
        
        this.listaReportes.removeAllItems();
        this.listaReportes.addItem("1 - Top 5 ingredientes mas caros");
    }
    
    public void llenarOrdenada(ChartPanel chartPanel) {
        SwingUtilities.invokeLater(() -> {
            this.panelReporte.removeAll(); //Se remueve la gr√°fica anterior
            chartPanel.setSize(this.panelReporte.getWidth(), this.panelReporte.getHeight());
            chartPanel.setPreferredSize(this.panelReporte.getSize());

            this.panelReporte.setLayout(new java.awt.BorderLayout());
            this.panelReporte.add(chartPanel, BorderLayout.CENTER);

            this.panelReporte.revalidate(); //Se vuelve a calcular el layout
            this.panelReporte.repaint();    //Se redibuja el panel
        });
    }
    
